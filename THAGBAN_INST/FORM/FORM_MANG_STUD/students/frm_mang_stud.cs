        public void get_data()
        {


            // This line of code is generated by Data Source Configuration Wizard
            // Instantiate a new DBContext
            THAGBAN_INST.DATA.db_max_instEntities dbContext = new THAGBAN_INST.DATA.db_max_instEntities();
            // Call the LoadAsync method to asynchronously get the data for the given DbSet from the database.
            dbContext.TBL_STUDENTS.LoadAsync().ContinueWith(loadTask =>
            {

                // Bind data to control when loading complete
                gridControl1.DataSource = dbContext.TBL_STUDENTS.Local.ToBindingList();
            }, System.Threading.Tasks.TaskScheduler.FromCurrentSynchronizationContext());
        }
        void get_sp()
        {
            // This line of code is generated by Data Source Configuration Wizard
            // Instantiate a new DBContext
            THAGBAN_INST.DATA.db_max_instEntities dbContext = new THAGBAN_INST.DATA.db_max_instEntities();
            // Call the LoadAsync method to asynchronously get the data for the given DbSet from the database.
            dbContext.TBL_STUDENTS.LoadAsync().ContinueWith(loadTask =>
            {

                var tenp = (from stud in dbContext.TBL_STUDENTS
                            join sp in dbContext.TBL_STUD_SPEC
                            on stud.STUD_ID equals sp.STUD_ID
                            join spec in dbContext.TBL_SPECIAL
                            on sp.SPEC_ID equals spec.SPEC_ID
                            select new
                            {
                                stud.STUD_ID,
                                stud.STUD_NAME,
                                stud.STUD_GENDER,
                                stud.STUD_ADDRESS,
                                stud.STUD_LNAME,
                                stud.STUD_DATE,
                                stud.STUD_PHONE,
                                stud.STUD_STATE,
                                spec.SPEC_NAME,
                                sp.STUD_SPEC_YEAR
                            }).ToList();

                // Bind data to control when loading complete
                gridControl1.DataSource = tenp;
            }, System.Threading.Tasks.TaskScheduler.FromCurrentSynchronizationContext());
        }
        void get_sele()
        {
            if (gridView2.SelectedRowsCount > 0)
            {


                stud_name = gridView2.GetFocusedRowCellValue("STUD_NAME").ToString();
                stud_lname = gridView2.GetFocusedRowCellValue("STUD_LNAME").ToString();
                stud_phone = gridView2.GetFocusedRowCellValue("STUD_PHONE").ToString();
                string aa = gridView2.GetFocusedRowCellValue("STUD_DATE").ToString();
                stud_date = DateTime.Parse(aa);
                stud_address = gridView2.GetFocusedRowCellValue("STUD_ADDRESS").ToString();
                stud_gender = gridView2.GetFocusedRowCellValue("STUD_GENDER").ToString();
                stud_id = Convert.ToInt32(gridView2.GetFocusedRowCellValue("STUD_ID").ToString());
                // holidaytype_id= Convert.ToInt32(gridView2.GetFocusedRowCellValue("DEPT_ID").ToString());
                string SP = "";
                if (gridView2.GetFocusedRowCellValue("SPEC_NAME") != null)
                    SP = gridView2.GetFocusedRowCellValue("SPEC_NAME").ToString();
